Map::Map()
{
    dirt = texture::LoadTexture("image/dirt.png");
    if (dirt == NULL) cout <<SDL_GetError();
    grass = texture::LoadTexture("image/grass.png");
    stone = texture::LoadTexture("image/stone.png");
    red_grass = texture::LoadTexture("image/red_grass.png");


    src.x = 0;
    src.y = 0;
    src.h = 16;
    src.w = 16;

    dest.x = 0;
    dest.y = 0;
    dest.h = 16;
    dest.w = 16;

    LoadMap(lv1);
}


void Map::LoadMap(int arr[240][60]) {
    for (int row = 0; row < 240; row++) {
        for (int column = 0; column < 60; column++) {
            mapping[row][column] = arr[row][column];

            tile[row][column].x = column * 16;
            tile[row][column].y = row * 16;
            tile[row][column].w = TILE_SIZE;
            tile[row][column].h = TILE_SIZE;
        }
    }
}



bool Map::checkCollision(SDL_Rect a, SDL_Rect b) {
    int leftA, leftB;
    int rightA, rightB;
    int topA, topB;
    int bottomA, bottomB;

    leftA = a.x;
    rightA = a.x + a.w;
    topA = a.y;
    bottomA = a.y + a.h;

    leftB = b.x;
    rightB = b.x + b.w;
    topB = b.y;
    bottomB = b.y + b.h;

    if (bottomA <= topB) {
        return false;
    }

    if (topA >= bottomB) {
        return false;
    }

    if (rightA <= leftB) {
        return false;
    }

    if (leftA >= rightB) {
        return false;
    }

    return true;
}


void Map::DrawMap(SDL_Rect Camera) {
    int type = 0;
    for (int row = 0; row < 240; row++) {
        for (int column = 0; column < 60; column++){
            type = mapping[row][column];

            dest.x = column * 16;
            dest.y = row * 16;
            if (checkCollision(Camera, tile[row][column]))
            {
                dest.y -= Camera.y;
                switch (type)
                {
                case 0:
                    {
                        texture::Draw(dirt, src, dest);
                        break;
                    }
                case 1:
                    {
                        texture::Draw(grass, src, dest);
                        break;
                    }
                case 2:
                    {
                        texture::Draw(stone, src, dest);
                    break;
                    }
                case 3:
                    break;

                case 8:
                    {
                        texture::Draw(red_grass, src, dest);
                        break;
                    }
                default:
                    break;
                }
            }
        }
    }

}

void Map::CloseMap()
{
    SDL_DestroyTexture(dirt);
    SDL_DestroyTexture(stone);
    SDL_DestroyTexture(grass);
    SDL_DestroyTexture(red_grass);
    dirt = NULL;
    stone = NULL;
    grass = NULL;
    red_grass = NULL;
}

